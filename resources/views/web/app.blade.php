performance of the marketing strategy.
 From this vantage point, we can identify the following key metrics:</p>
<ul>
<li class="mt-4 family">Metrics for measuring notoriety, such as the number of followers, page views, reach,
 impressions, website visits, and much more, are used to analyse the number of users reached in a given period.
 The number of followers, for example, is determined not only by social activity, but also by how much a brand is exposed through other channels 
(for example, advertising, SEO, and public relations).</li>

<li class="mt-4 family">Metrics for measuring engagement, or how much users participate in the published content. 
Some metrics to consider include: post comments, reactions, and sharing, website pages visited, time spent on the site, 
and bounce rate. The amount of time spent on the site and the frequency with which visitors bounce can indicate whether or not a
 piece of content is effective.
 Long periods of time spent on the site indicate a high level of user involvement.</li>
 <li class="mt-4 family">Metrics for measuring conversions, such as how many users have reached the final stage of the funnel, i.e.
 the decision to purchase the offered product or service. Sales, request for quotes, and contact requests
 are examples of such metrics.</li>
</ul>

